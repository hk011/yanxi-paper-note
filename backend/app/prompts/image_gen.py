# 科研论文常见示意图类型（调用 gen_figure 时按内容选用其一）
GEN_FIGURE_TYPES = """
- 机制图：展示因果关系、作用路径、生物/物理/化学机制
- 模型架构图：神经网络层叠、模块组成、特征变换（深度学习论文最常见）
- 算法流程图：步骤、条件分支、循环、决策节点
- 系统管线图：多阶段数据处理/训练/推理流水线，左→右或上→下
- 编码器-解码器图：对称或 U 形结构、跳跃连接、瓶颈层
- 方法总览图：整篇方法的高层模块关系与数据流向
- 技术路线图：研究框架、阶段划分、里程碑
- 对比方案图：原有方案 vs 改进方案，左右并列
- 时序交互图：多方协议、模块间调用顺序、握手流程
- 多分支合并图：多模态输入、多尺度特征、集成后汇聚
- 层次结构图：分层模块、树状或垂直分层（端→云→链等）
- Agent/工作流图：智能体协作、工具调用链、任务分解
""".strip()

GEN_FIGURE_PROMPT_TEMPLATE = """请生成一张用于论文的【图的类型】，主题是【研究主题】。
图中展示【主体对象】、【关键过程 1】、【关键过程 2】以及【最终结果】。
请采用【构图方式】布局，并通过箭头、局部放大视图或分层结构来强化机制表达。
整体风格要求简洁、专业、适合论文发表，呈现高质量科研示意图风格，具有清晰的结构层级和良好的可读性。
请使用【背景要求】，以【主色】为主色，并用【强调色】突出关键事件/创新模块。
需要标注的中文标签放在引号内。"""

GEN_FIGURE_TOOL_DESC = f"""根据描述生成配图（Seedream），用于你认为「画图比纯文字更好理解」的段落。
由你决定画什么、图题叫什么；优先用论文原图，本工具作补充示意。

调用时必须按以下模板组织 prompt（将【】内占位符替换为具体内容，不要保留【】）：
{GEN_FIGURE_PROMPT_TEMPLATE}

常见图的类型（按内容选用）：
{GEN_FIGURE_TYPES}

构图方式示例：左→右流水线、上→下流程、U 形迭代环、左右对比、垂直分层、三栏映射、中心辐射。
背景要求示例：纯白底、浅灰底。
主色/强调色示例：低饱和蓝灰为主、橙色/绿色高亮创新模块。
可选 ref_image_path 传入相关论文原图路径作参考。"""

GEN_FIGURE_PROMPT_SUFFIX = (
    "，清晰克制的学术示意风格，结构清楚，中文标签可读，白底，无装饰水印"
)


def enhance_figure_prompt(user_prompt: str) -> str:
    """为模型简述补上统一视觉基调，避免风格漂移。"""
    text = (user_prompt or "").strip()
    if not text:
        return text
    if "白底" in text or "示意" in text or "论文发表" in text:
        return text
    return f"{text}{GEN_FIGURE_PROMPT_SUFFIX}"
